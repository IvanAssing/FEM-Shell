IntegralGauss2D::IntegralGauss2D()
{
    n = -1;
}

IntegralGauss2D::IntegralGauss2D(int n_)
    :n(n_)
{
    p = new __float128[n];
    w = new __float128[n];


    __float128 z,z1,pp,p1,p2,p3;


    __float128 EPS = 1.0e-27q;

    __float128 x1 = -1.0q;
    __float128 x2 = 1.0q;

    __float128 xm = (x1+x2)/2.0q;
    __float128 xl = (x2-x1)/2.0q;


    //    Algoritmo adaptado do Numerical Recipes pg. 189
    //    PRESS, WILLIAN H., ET.AL. Numerical Recipes: The Art of Scientific Computing, 3 ed.,
    //    Cambridge University Press, New York,2007.

    //    Este algoritmo foi adaptado para usar número ponto flutuante de 128 bits
    //    Oferece respostas com 28 casas decimais de precisão.

    //    Programador: Ivan Assing
    //    Email: ivanassing@gmail.com
    //    Data: 06/05/2010


    for(int i=0; i<(n+1)/2; i++)
    {
        z = cosq(M_PIq*(i+0.75q)/(n+0.5q));

        do{
            p1 = 1.0q;
            p2 = 0.0q;
            for(int j=0;j<n;j++)
            {
                p3 = p2;
                p2 = p1;
                p1 = ((2.0q*j+1.0q)*z*p2-j*p3)/(j+1);
            }

            pp = n*(z*p1-p2)/(z*z-1.0q);
            z1 = z;
            z = z1-p1/pp;
        }while((z-z1)>0.0q? (z-z1)>EPS:(z1-z)>EPS);
        p[i] = xm-xl*z;
        p[n-1-i] = xm+xl*z;
        w[i] = 2.0q*xl/((1.0q-z*z)*pp*pp);
        w[n-1-i] = w[i];

    }


    //    std::cout<<"\n\n-> "<<n;
    //    for(int i=0; i<n; i++)
    //        for(int j=0; j<n; j++)
    //            std::cout<<"\n"<<print(w[i]*w[j])<<" * f( "<<print(p[i])<<" # "<<print(p[j])<<" ) + ";


}


double IntegralGauss2D::operator()(Functor2D &f)
{
    double sum = 0.0;

    for(int i=0; i<n; i++)
        for(int j=0; j<n; j++)
            sum += w[i]*w[j] * f(p[i], p[j]);

    return sum;
}



